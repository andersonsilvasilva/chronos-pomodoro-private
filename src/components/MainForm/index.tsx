import { PlayCircleIcon, StopCircleIcon } from 'lucide-react';
import { Cycles } from '../Cycles';
import { DefaultButton } from '../DefaultButton';
import { DefaultInput } from '../DefaultInput';
import React, { useRef} from 'react';
import { useTaskContext } from '../../contexts/TaskContext/useTaskContext';
import type { TaskModel } from '../../models/TaskModel';
import { getNextCycle } from '../../utils/getNextCycle';
import { getNextCycleType } from '../../utils/getNextCycleType';
import { TaskActionTypes } from '../../contexts/TaskContext/taskActions';


export function MainForm() {
  const { state, dispatch} = useTaskContext();
  const taskNameInput = useRef<HTMLInputElement>(null);

  //ciclos
  const nextCycle = getNextCycle(state.currentCycle);
  const nextCycleType = getNextCycleType(nextCycle);
  

  console.log('nextCycle', nextCycle);

  function handleCreateNewTask(event: React.FormEvent<HTMLFormElement>) {
    event.preventDefault();

    if (taskNameInput.current === null) return;

    const taskName = taskNameInput.current.value.trim();

    if (!taskName) {
      alert('Digite o nome da tarefa');
      return;
    }

    const newTask: TaskModel = {
      id: Date.now().toString(),
      name: taskName,
      startDate: Date.now(),
      completeDate: null,
      interruptDate: null,
      duration: state.config[nextCycleType],
      type: nextCycleType,
    };


    dispatch({ type: TaskActionTypes.START_TASK, payload:newTask});


  }
  function handleInterruptCurrentTask( e:React.MouseEvent<HTMLButtonElement, MouseEvent>) {
    e.preventDefault();
    
    // setState(prevState => {
    //   return {
    //     ...prevState,
    //     activeTask: null,
    //     secondsRemaining: 0,
    //     formattedSecondsRemaining: '00:00',
    //     tasks: prevState.tasks.map(task => {
    //       if  (prevState.activeTask && prevState.activeTask.id === task.id){
    //         return { ...task, interruptDate: Date.now()};
    //       }
    //       return task;
    //     }),
    //   };
    // });
  }
  
  return (
    <form onSubmit={handleCreateNewTask} className='form' action=''>
      <div className='formRow'>
        <DefaultInput
          labelText='task'
          id='meuInput'
          type='text'
          placeholder='Digite algo'
          ref={taskNameInput}
          disabled={!!state.activeTask}
          
        />
      </div>

      <div className='formRow'>
        <p>Próximo intervalo é de 25min</p>
      </div>
      {state.currentCycle >0 && (    
        <div className='formRow'>
          <Cycles />
        </div>
      )}

      <div className='formRow'>
        {!state.activeTask ? (
            <DefaultButton aria-label='Iniciar nova tarefa' 
            title='Iniciar nova tarefa' 
            type='submit' 
            icon={<PlayCircleIcon />} 
          />
        ) : (
          <DefaultButton aria-label='Interroper tarefa atual' 
            title='Interroper tarefa atual' 
            type='button' 
            color='red' 
            icon={<StopCircleIcon />} 
            onClick={handleInterruptCurrentTask}
          />

        )}
      </div>
    </form>
  );
}